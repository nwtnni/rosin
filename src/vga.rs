use core::fmt;
use core::fmt::Write as _;

pub mod buffer;
pub mod color;
pub mod screen;

#[macro_export]
macro_rules! print {
    ($($arg:tt)*) => ($crate::vga::_print(format_args!($($arg)*)));
}

#[macro_export]
macro_rules! println {
    () => ($crate::print!("\n"));
    ($($arg:tt)*) => ($crate::print!("{}\n", format_args!($($arg)*)));
}

#[doc(hidden)]
pub fn _print(args: fmt::Arguments) {
    screen::WRITER.lock().write_fmt(args).unwrap();
}
